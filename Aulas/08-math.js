let a = 10;
let b = 2;

console.log(`${a} + ${b} = ${a + b}`);      /** soma */
console.log(`${a} - ${b} = ${a - b}`);      /** subtração */
console.log(`${a} * ${b} = ${a * b}`);      /** multiplicação */
console.log(`${a} / ${b} = ${a / b}`);      /** divisão */
console.log(`${a} % ${b} = ${a % b}`);      /** resto */
console.log(`${a} ^ ${b} = ${a ** b}`);     /** Exponencial */